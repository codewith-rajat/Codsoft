num1=int(input("Enter the first number: "))
operation=input("Enter the Operator: ")
num2=int(input("Enter the second number: "))
if operation=="+":
    print(num1+num2)
elif operation=="-":
    print(num1-num2)
elif operation=="*":
    print(num1*num2)
elif operation=="/":
    print(num1/num2)
elif operation=="%":
    print(num1%num2)
